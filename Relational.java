public class Relational {
    public static void main(String[] args) {
        float a=2.55f;
        float b=3.00f;
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(b>a);
        System.out.println(b<a);
    }
}
